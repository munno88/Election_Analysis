score = int(input("what is your test score?"))

# determine the grade

if score >=90:

    print ("Your Grade is an A.")

else:

    if score >=80:

        print("Your Grade is an B.")

    else:

        if score >=70:

            print("Your Grade is an C.")

        else:

            if score >=60:

                print("your Grade is an D.")

            else:
                print("You Grade is an F.")